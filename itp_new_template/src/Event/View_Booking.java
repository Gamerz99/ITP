    private void bookingMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bookingMouseClicked
        // TODO add your handling code here:
         try {
             
             int row=booking.getSelectedRow();
             String no=booking.getModel().getValueAt(row, 0).toString();
             String b="select *from event_booking where booking_ID="+no+"";
             pst=conn.prepareStatement(b);
             table=pst.executeQuery();
             if(table.next())
             {
                String bd=table.getString("Booked_Date");
                bookeddate.setText(bd);
                Extra.setText(table.getString("Extra"));
                cuscontact.setText(table.getString("Cus_Contact"));
                bookingid.setText(no);
                cusname.setText(table.getString("Cus_Name"));
                cusnic.setText(table.getString("Cus_NIC"));
                pstatus.setText(table.getString("Payment_Status"));
                total.setText(table.getString("Total"));
                etype.addItem(table.getString("Event_Type"));
                menuno.setText(table.getString("Menu_NO"));
                 
                String date=table.getString("Event_Date");
                Date edate=new SimpleDateFormat("yyyy-MM-dd").parse((String) date);
                eventdate.setDate(edate);
               
                //((JTextField)eventdate.getDateEditor().getUiComponent()).setText(date);
             }
         } catch (SQLException ex) {
            System.out.println(ex);
         } catch (ParseException ex) {
             Logger.getLogger(View_Booking.class.getName()).log(Level.SEVERE, null, ex);
         }                                   
    }//GEN-LAST:event_bookingMouseClicked

